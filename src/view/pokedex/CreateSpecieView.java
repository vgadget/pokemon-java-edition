    private Sprite generateDefaultSprite(String pathname) throws Exception {

        File f = new File(pathname);

        File[] files = f.listFiles();

        List<BufferedImage> animation = new LinkedList<>();

        Arrays.sort(files, new Comparator<File>() {
            @Override
            public int compare(File o1, File o2) {

                return new StringComparator().compare(o1.getName(), o2.getName());
            }
        });

        for (File file : files) {

            if (file.isFile()) {

                try {
                    animation.add(ImageIO.read(file));
                } catch (Exception e) {
                }

            }

        }

        BufferedImage imgs[] = new BufferedImage[animation.size()];

        for (int i = 0; i < animation.size(); i++) {

            imgs[i] = utilities.image.ImageUtil.resize(animation.get(i), 300, 300);

        }

        return new Sprite(imgs, this.jSliderRefreshRate.getModel().getMaximum() - ((int) Long.parseLong(this.jLabelSpriteSpeed.getText())) + 1);
    }

