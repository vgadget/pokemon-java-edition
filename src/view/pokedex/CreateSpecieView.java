    private void jButtonLoadCryAudioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoadCryAudioActionPerformed

        String desktopDir = System.getProperty("user.home") + "/Desktop";

        JFileChooser fileChooser = new JFileChooser(desktopDir);

        fileChooser.setFileFilter(new FileNameExtensionFilter("WAV Audio File (.WAV)", "wav"));

        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {

            File audiofile = fileChooser.getSelectedFile();

            try {
                Sound cry = new Sound(audiofile);

                if (cry.getClip().getMicrosecondLength() <= 1e+7) { // The maximum cry length must be 10 seconds or less
                    this.loadedCry = cry;
                } else {
                    utilities.DisplayMessage.showErrorDialog("The maximum cry length must be 10 seconds or less");
                }

            } catch (Exception ex) {
                Logger.getLogger(CreateSpecieView.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
    }//GEN-LAST:event_jButtonLoadCryAudioActionPerformed

